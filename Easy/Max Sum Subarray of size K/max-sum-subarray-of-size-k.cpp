class Solution{   
public:
    long maximumSumSubarray(int K, vector<int> &Arr , int N){
        long long ans=0;
        for(int i=0; i<K; i++){
            ans+=Arr[i];
        }
        long long x=ans;
        for(int i=K, j=0; i<N; i++, j++){
            x+=Arr[i]-Arr[j];
            ans=max(ans, x);
        }
        return ans;
    }
};

